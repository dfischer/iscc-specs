def test_code_image_bits32():
    cidi32 = iscc.code_image(images()[0], image_bits=32)
    assert cidi32 == dict(code="EEAMGQZQTY", width=200, height=133)
    c1 = Code(cidi32["code"])
    assert c1.length == 32
    cidi64 = iscc.code_image(images()[0], image_bits=32)
    c2 = Code(cidi64["code"])
    assert c1 ^ c2 == 0


